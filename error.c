#include "error.h"

s32 row = 1;
s32 col = 0;

s32 col_incr = 0;

void Char_Move(s32 n)
{
	col += col_incr;
	col_incr = n;
}

void Char_Newline(void)
{
	row++;
	col = 0;
	col_incr = 0;
}
